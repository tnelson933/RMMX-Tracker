interface StateSelectProps {
  value: string | null | undefined;
  onChange: (value: string) => void;
  placeholder?: string;
}

export function StateSelect({
  value,
  onChange,
  placeholder = "Select state…",
}: StateSelectProps) {
  const [open, setOpen] = useState(false);
  const upper = (value ?? "").toUpperCase();
  const selected = US_STATES.find((s) => s.code === upper);

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <Button
          type="button"
          variant="outline"
          role="combobox"
          aria-expanded={open}
          className="w-full justify-between font-normal h-9 px-3 text-sm shadow-sm"
        >
          {selected ? (
            <span>
              <span className="font-mono">{selected.code}</span>
              {" — "}
              {selected.name}
            </span>
          ) : (
            <span className="text-muted-foreground">{placeholder}</span>
          )}
          <ChevronsUpDown className="ml-2 h-4 w-4 shrink-0 opacity-50" />
        </Button>
      </PopoverTrigger>
      <PopoverContent className="w-[270px] p-0" align="start">
        <Command>
          <CommandInput placeholder="Type state name or code…" />
          <CommandList className="max-h-60">
            <CommandEmpty>No state found.</CommandEmpty>
            <CommandGroup>
              {US_STATES.map((s) => (
                <CommandItem
                  key={s.code}
                  value={`${s.code} ${s.name}`}
                  onSelect={() => {
                    onChange(s.code);
                    setOpen(false);
                  }}
                >
                  <Check
                    className={cn(
                      "mr-2 h-4 w-4 shrink-0",
                      upper === s.code ? "opacity-100" : "opacity-0",
                    )}
                  />
                  <span className="font-mono text-xs w-7 mr-1 shrink-0">
                    {s.code}
                  </span>
                  <span>{s.name}</span>
                </CommandItem>
              ))}
            </CommandGroup>
          </CommandList>
        </Command>
      </PopoverContent>
    </Popover>
  );
}
